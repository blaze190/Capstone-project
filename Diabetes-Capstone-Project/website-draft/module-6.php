			Feeling excessively thirsty, Frequently needing to wee, Feeling tired, Blurred vision, Weight loss, Hunger, Mood changes.
			<br>What can cause high BSLs; Ilness; Infection; Stress, worry and anxiety; Too many carbohydrates at once; Not enough insulin; Less exercise than usual
			<br>What short-term problems can occur with hyperglycaemia? If hyperglycemia is left untreated ketoacidosis may develop. Ketoacidosis (DKA) is a serious condition associated with illness or very high blood glucose levels in type 1 diabetes. 
			It develops gradually over hours or days. It is a sign of insufficient insulin. 
			Without enough insulin, the body’s cells cannot use glucose for energy. To make up for this, the body begins to burn fat for energy instead.
			This leads to accumulation of dangerous chemical substances in the blood called ketones, which also appear in the urine.
			<br>Signs of DKA include: rapid breathing, flushed cheeks, abdominal pain, vomiting and dehydration. </p>
