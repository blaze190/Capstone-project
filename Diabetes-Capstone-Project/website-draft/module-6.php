          <p class="">When do I use Insulin? <br>
			What does a hypo feel like? Everyone feels different when having a hypo. Some common signs include feeling: Sweaty, Shaky, Sleepy, Cross, Weak, Like it's hard to concentrate, Have a headache, Emotional, 
			What can cause a hypo?
			Too much insulin, Not eating enough carbohydrates, Skipping meals, Exercise or sport, Stress or anxiety.
			Sometimes you can have a hypo for no reason at all!
			Common times for hypos are just before lunch and during and after sport. They can also happen during the night if your blood sugar is too low before you go to bed.
			<br>What should I do if I think I’m having a hypo?
			If you feel like you are having a hypo, the first thing you should do is always eat or drink something sweet. Appropriate sweet foods are fast acting carbohydrates, such as:
			½ glass of fruit juice, cordial or soft drink;
			4 large or 7 small jelly beans;
			3 teaspoons honey or sugar;
			<br>The second thing you need to do is check your BSL. If you are having a hypo it will be below 4.
			After 15 minutes, you should have a long-acting carbohydrate to stop the hypo from coming back, like:
			A piece of fruit, 
			A slice of bread,
			Two biscuits, or
			A glass of milk.
			<br>
			Finally, it is important to check your BSL a second time 15-30mins later. It should be improving. If hypoglycaemia is left untreated or does not improve you may fall asleep or have a fit.

