          <h2 class="text-primary pt-3">Long term...</h2>
			<br>What long-term problems can occur with hyperglycaemia?
			<br>When blood glucose is too high too often, it can harm blood vessels and cause heart attacks. It can also damage organs in the body and cause blindness, kidney failure, or loss of toes or feet.
			<br>What do I do if my blood sugar is too high?
			<br>If your blood sugar is too high, you need to lower it by adjusting the amount of insulin you take with your next meal. You also should drink plenty of fluids because hyperglycaemia can make you dehydrated. 
			<br>If your blood sugar is very high (above 15mmol/L) you need to check for ketones as well. You can do this by testing your urine with strips, or by using a special glucometer with a drop of blood. 
			<br>If you have lots of ketones in your urine (above 1.5mmol/L), you are in the early stages of DKA. You need to call your diabetes educator to work out how much extra insulin you need to take. 
			<br>If you don’t have lots of ketones, keep checking your BSL and ketones every 2 hours until your BSL is below 7. Do not exercise if your BSL is above 15mmol/L or ketones are present.
